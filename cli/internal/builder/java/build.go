// ---- Types file ----

func generateTypes(artifact *types.CompiledArtifact, pkgName, rootPkg, enginePkg string) (string, error) {
	var b strings.Builder
	writeJavaHeader(&b, artifact)
	fmt.Fprintf(&b, "package %s.%s;\n\n", rootPkg, pkgName)
	// Import engine Types by simple name so the stream delegate can refer to
	// Types.TokenStream. A fully-qualified nested name
	// (…Types.TokenStream) is mis-resolved by Eclipse JDT when the package
	// path contains a "java" segment (aifunc._engine.java.…).
	if isStreamOutput(artifact) {
		fmt.Fprintf(&b, "import %s.Types;\n", enginePkg)
	}
	b.WriteString("import java.util.List;\nimport java.util.Map;\n\n")
	b.WriteString("/** Generated by aifn — do not edit manually. */\n")
	fmt.Fprintf(&b, "public final class %sTypes {\n\n", toPascalCase(artifact.Package.Name))
	fmt.Fprintf(&b, "%sprivate %sTypes() {}\n\n", javaIndent, toPascalCase(artifact.Package.Name))

	baseName := toPascalCase(artifact.API.Name)
	writeJavaPojo(&b, baseName+"Input", artifact.API.Input)
	b.WriteString("\n")
	if isStreamOutput(artifact) {
		writeTokenStreamDelegate(&b)
	} else {
		writeJavaPojo(&b, baseName+"Output", artifact.API.Output)
	}
	b.WriteString("}\n")
	return b.String(), nil
}

func writeJavaPojo(b *strings.Builder, className string, schema map[string]any) {
	props, _ := schema["properties"].(map[string]any)
	required := requiredSet(schema)

	// required fields first, then optional — determines constructor parameter order
	var reqKeys, optKeys []string
	for _, k := range sortedKeys(props) {
		if required[k] {
			reqKeys = append(reqKeys, k)
		} else {
			optKeys = append(optKeys, k)
		}
	}
	allKeys := append(reqKeys, optKeys...)

	fmt.Fprintf(b, "%spublic static final class %s {\n", javaIndent, className)

	for _, key := range allKeys {
		prop, ok := props[key].(map[string]any)
		if !ok {
			continue
		}
		desc, _ := prop["description"].(string)
		jType := jsonSchemaToJavaType(prop)
		field := toCamelCase(key)
		if desc != "" {
			fmt.Fprintf(b, "%s%s/** %s */\n", javaIndent, javaIndent, desc)
		}
		if required[key] {
			fmt.Fprintf(b, "%s%sprivate final %s %s;\n", javaIndent, javaIndent, jType, field)
		} else {
			fmt.Fprintf(b, "%s%sprivate %s %s;\n", javaIndent, javaIndent, jType, field)
		}
	}
	b.WriteString("\n")

	// all-arg constructor: required params first, then optional
	var params []string
	for _, key := range allKeys {
		if prop, ok := props[key].(map[string]any); ok {
			params = append(params, jsonSchemaToJavaType(prop)+" "+toCamelCase(key))
		}
	}

	// No-arg constructor is only legal in Java when there are no final (required)
	// fields — every final must be assigned in every constructor.
	if len(reqKeys) == 0 {
		fmt.Fprintf(b, "%s%spublic %s() {}\n\n", javaIndent, javaIndent, className)
	}

	// Skip a second empty constructor when we already emitted no-arg above.
	if len(params) > 0 || len(reqKeys) > 0 {
		fmt.Fprintf(b, "%s%spublic %s(%s) {\n", javaIndent, javaIndent, className, strings.Join(params, ", "))
		for _, key := range allKeys {
			if _, ok := props[key].(map[string]any); !ok {
				continue
			}
			field := toCamelCase(key)
			fmt.Fprintf(b, "%s%s%sthis.%s = %s;\n", javaIndent, javaIndent, javaIndent, field, field)
		}
		fmt.Fprintf(b, "%s%s}\n\n", javaIndent, javaIndent)
	}

	// getters / setters
	for _, key := range allKeys {
		prop, ok := props[key].(map[string]any)
		if !ok {
			continue
		}
		jType := jsonSchemaToJavaType(prop)
		field := toCamelCase(key)
		cap := strings.ToUpper(field[:1]) + field[1:]
		fmt.Fprintf(b, "%s%spublic %s get%s() { return %s; }\n", javaIndent, javaIndent, jType, cap, field)
		if !required[key] {
			fmt.Fprintf(b, "%s%spublic void set%s(%s v) { this.%s = v; }\n", javaIndent, javaIndent, cap, jType, field)
		}
	}
	fmt.Fprintf(b, "%s}\n", javaIndent)
}


// writeTokenStreamDelegate emits a public TokenStream class inside the *Types
// outer class. It wraps the engine-internal TokenStream so callers only need to
// import from the package-level types file (e.g. ChatStreamTypes.TokenStream)
// and never from the _engine internal package.
//
// Relies on a file-level `import …Types` (see generateTypes) and refers to the
// engine type as Types.TokenStream — not a fully-qualified nested name.
func writeTokenStreamDelegate(b *strings.Builder) {
	fmt.Fprintf(b, "%s/**\n", javaIndent)
	fmt.Fprintf(b, "%s * A blocking token iterator returned by this package's stream method.\n", javaIndent)
	fmt.Fprintf(b, "%s * Use it in a try-with-resources block; a {@code break} cancels the\n", javaIndent)
	fmt.Fprintf(b, "%s * stream automatically and frees the background thread.\n", javaIndent)
	fmt.Fprintf(b, "%s */\n", javaIndent)
	fmt.Fprintf(b, "%spublic static final class TokenStream implements java.util.Iterator<String>, AutoCloseable {\n", javaIndent)
	fmt.Fprintf(b, "%s%sprivate final Types.TokenStream delegate;\n\n", javaIndent, javaIndent)
	fmt.Fprintf(b, "%s%spublic TokenStream(Types.TokenStream delegate) { this.delegate = delegate; }\n", javaIndent, javaIndent)
	fmt.Fprintf(b, "%s%spublic TokenStream(java.util.Iterator<String> iterator, Runnable cancelFn) { this.delegate = new Types.TokenStream(iterator, cancelFn); }\n\n", javaIndent, javaIndent)
	fmt.Fprintf(b, "%s%s@Override public boolean hasNext() { return delegate.hasNext(); }\n", javaIndent, javaIndent)
	fmt.Fprintf(b, "%s%s@Override public String  next()    { return delegate.next();    }\n", javaIndent, javaIndent)
	fmt.Fprintf(b, "%s%s/** Cancels the stream. Idempotent. */\n", javaIndent, javaIndent)
	fmt.Fprintf(b, "%s%spublic void cancel()               { delegate.cancel();         }\n", javaIndent, javaIndent)
	fmt.Fprintf(b, "%s%s@Override public void close()      { delegate.close();          }\n", javaIndent, javaIndent)
	fmt.Fprintf(b, "%s}\n", javaIndent)
}
