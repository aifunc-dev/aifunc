func generateArtifact(artifact *types.CompiledArtifact, pkgName, rootPkg string) (string, error) {
	var b strings.Builder
	writeJavaHeader(&b, artifact)
	fmt.Fprintf(&b, "package %s.%s;\n\nimport java.util.List;\nimport java.util.Map;\n\n", rootPkg, pkgName)
	b.WriteString("/** Generated by aifn — do not edit manually. */\n")
	b.WriteString("@SuppressWarnings(\"serial\")\n")
	fmt.Fprintf(&b, "final class %sAifunc {\n\n%sprivate %sAifunc() {}\n\n", toPascalCase(artifact.Package.Name), javaIndent, toPascalCase(artifact.Package.Name))
	b.WriteString(javaIndent + "static final java.util.Map<String, Object> ARTIFACT_DATA =\n")
	b.WriteString(javaIndent + javaIndent + ToJavaMap(artifact, 2) + ";\n}\n")
	return b.String(), nil
}

