// isStreamOutput reports whether the artifact's output schema has x-delivery-mode: stream.
func isStreamOutput(artifact *types.CompiledArtifact) bool {
	mode, _ := artifact.API.Output["x-delivery-mode"].(string)
	return mode == "stream"
}

// Generate writes <Name>Types.java, <Name>Aifunc.java, <Name>AifuncMock.java,
// and <Name>.java for the given compiled artifact into outputDir.
// javaPackageRoot is the root Java package prefix (e.g. "aifunc").
func Generate(artifact *types.CompiledArtifact, outputDir string, hasMock bool, engineVersion string, cfg types.AifuncConfig, javaPackageRoot string) error {
	if err := os.MkdirAll(outputDir, dirPerm); err != nil {
		return fmt.Errorf("create output dir: %w", err)
	}

	baseName := toPascalCase(artifact.Package.Name)
	pkgName := sanitizePackageName(artifact.Package.Name)
	engineVer := "v" + strings.ReplaceAll(engineVersion, ".", "_")
	enginePkg := javaPackageRoot + "._engine.java." + engineVer

	type fileSpec struct {
		name      string
		generator func() (string, error)
	}

	files := []fileSpec{
		{baseName + "Types.java", func() (string, error) {
			return generateTypes(artifact, pkgName, javaPackageRoot, enginePkg)
		}},
		{baseName + "Aifunc.java", func() (string, error) {
			return generateArtifact(artifact, pkgName, javaPackageRoot)
		}},
	}

	if hasMock {
		files = append(files, fileSpec{baseName + "AifuncMock.java", func() (string, error) {
			return generateMockStub(artifact, pkgName, javaPackageRoot)
		}})
	}

	files = append(files, fileSpec{baseName + ".java", func() (string, error) {
		return generateEntry(artifact, pkgName, baseName, hasMock, enginePkg, cfg, javaPackageRoot)
	}})

	for _, f := range files {
		content, err := f.generator()
		if err != nil {
			return fmt.Errorf("generate %s: %w", f.name, err)
		}
		if err := os.WriteFile(filepath.Join(outputDir, f.name), []byte(content), filePerm); err != nil {
			return fmt.Errorf("write %s: %w", f.name, err)
		}
	}
	return nil
}

// GenerateRootTypes writes AIFuncConfig.java and AIFuncException.java to the
// output root directory (package "aifunc" or whatever javaPackageRoot is).
// These are shared across all installed packages.
func GenerateRootTypes(outputDir string, javaPackageRoot string) error {
	if err := os.MkdirAll(outputDir, dirPerm); err != nil {
		return fmt.Errorf("create output dir: %w", err)
	}

	cfgContent := generateAIFuncConfig(javaPackageRoot)
	if err := os.WriteFile(filepath.Join(outputDir, "AIFuncConfig.java"), []byte(cfgContent), filePerm); err != nil {
		return fmt.Errorf("write AIFuncConfig.java: %w", err)
	}

	excContent := generateAIFuncException(javaPackageRoot)
	if err := os.WriteFile(filepath.Join(outputDir, "AIFuncException.java"), []byte(excContent), filePerm); err != nil {
		return fmt.Errorf("write AIFuncException.java: %w", err)
	}
	return nil
}

