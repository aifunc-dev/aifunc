// ---- Entry file ----

// generateEntry dispatches to the streaming or non-streaming entry generator.
func generateEntry(artifact *types.CompiledArtifact, pkgName, baseName string, hasMock bool, enginePkg string, cfg types.AifuncConfig, rootPkg string) (string, error) {
	if isStreamOutput(artifact) {
		return generateStreamEntry(artifact, pkgName, baseName, hasMock, enginePkg, cfg, rootPkg)
	}
	return generateNonStreamEntry(artifact, pkgName, baseName, hasMock, enginePkg, cfg, rootPkg)
}

// generateStreamEntry generates the entry class for a streaming package.
// The exported method returns a TokenStream (Iterator<String> + AutoCloseable).
func generateStreamEntry(artifact *types.CompiledArtifact, pkgName, baseName string, hasMock bool, enginePkg string, cfg types.AifuncConfig, rootPkg string) (string, error) {
	var b strings.Builder

	funcPascal := toPascalCase(artifact.API.Name)
	funcCamel  := toCamelCase(artifact.API.Name)
	inputName  := funcPascal + "Input"
	typeClass  := baseName + "Types"

	writeJavaHeader(&b, artifact)
	fmt.Fprintf(&b, "package %s.%s;\n\n", rootPkg, pkgName)
	fmt.Fprintf(&b, "import %s.AIFuncConfig;\n", rootPkg)
	fmt.Fprintf(&b, "import %s.Runtime;\n", enginePkg)
	fmt.Fprintf(&b, "import %s.Artifact;\n", enginePkg)
	fmt.Fprintf(&b, "import %s.Types.AIFuncArtifact;\n", enginePkg)
	b.WriteString("import java.util.LinkedHashMap;\nimport java.util.Map;\n\n")

	desc := artifact.API.Description
	if desc == "" {
		desc = artifact.Package.Description
	}
	if desc != "" {
		fmt.Fprintf(&b, "/** %s */\n", desc)
	}
	b.WriteString("/** Generated by aifn — do not edit manually. */\n")
	fmt.Fprintf(&b, "public final class %s {\n\n", baseName)
	fmt.Fprintf(&b, "%sprivate %s() {}\n\n", javaIndent, baseName)

	if hasMock {
		fmt.Fprintf(&b, "%sprivate static final Object MOCK_DATA = %sAifuncMock.MOCK_DATA;\n\n", javaIndent, baseName)
	}

	fmt.Fprintf(&b, "%s/**\n%s * Stream tokens from the model.\n", javaIndent, javaIndent)
	fmt.Fprintf(&b, "%s * Returns a {@link %s.TokenStream} — use it in a try-with-resources block;\n", javaIndent, typeClass)
	fmt.Fprintf(&b, "%s * {@code break} automatically cancels the stream and frees the background thread.\n%s */\n", javaIndent, javaIndent)
	fmt.Fprintf(&b, "%spublic static %s.TokenStream %s(AIFuncConfig config, %s.%s input) {\n", javaIndent, typeClass, funcCamel, typeClass, inputName)
	writeConfigWiring(&b, hasMock, cfg, baseName)
	fmt.Fprintf(&b, "%s%sAIFuncArtifact artifact = Artifact.fromMap(%sAifunc.ARTIFACT_DATA);\n", javaIndent, javaIndent, baseName)
	fmt.Fprintf(&b, "%s%sMap<String, Object> inputMap = inputToMap(input);\n", javaIndent, javaIndent)
	fmt.Fprintf(&b, "%s%sreturn new %s.TokenStream(Runtime.executeStream(artifact, inputMap, config));\n", javaIndent, javaIndent, typeClass)
	fmt.Fprintf(&b, "%s}\n\n", javaIndent)

	fmt.Fprintf(&b, "%sprivate static Map<String, Object> inputToMap(%s.%s input) {\n", javaIndent, typeClass, inputName)
	fmt.Fprintf(&b, "%s%sMap<String, Object> m = new LinkedHashMap<>();\n", javaIndent, javaIndent)
	inputProps, _ := artifact.API.Input["properties"].(map[string]any)
	for _, key := range sortedKeys(inputProps) {
		field := toCamelCase(key)
		getter := "get" + strings.ToUpper(field[:1]) + field[1:]
		if isRequired(key, artifact.API.Input) {
			fmt.Fprintf(&b, "%s%sm.put(%q, input.%s());\n", javaIndent, javaIndent, key, getter)
		} else {
			fmt.Fprintf(&b, "%s%sif (input.%s() != null) m.put(%q, input.%s());\n", javaIndent, javaIndent, getter, key, getter)
		}
	}
	fmt.Fprintf(&b, "%s%sreturn m;\n%s}\n}\n\n", javaIndent, javaIndent, javaIndent)

	return b.String(), nil
}


// buildConfigWiring is a no-op placeholder used to avoid unused-variable warnings.
func buildConfigWiring(_ *strings.Builder, _ bool, _ types.AifuncConfig, _ string) string {
	return ""
}

// writeConfigWiring emits the null-config / mock-data wiring block.
func writeConfigWiring(b *strings.Builder, hasMock bool, cfg types.AifuncConfig, baseName string) {
	if hasMock {
		fmt.Fprintf(b, "%s%sif (config == null) {\n", javaIndent, javaIndent)
		fmt.Fprintf(b, "%s%s%sconfig = AIFuncConfig.builder().mock(true).mockData(MOCK_DATA).build();\n", javaIndent, javaIndent, javaIndent)
		fmt.Fprintf(b, "%s%s} else if (config.isMock() && config.getMockData() == null) {\n", javaIndent, javaIndent)
		fmt.Fprintf(b, "%s%s%sconfig = config.toBuilder().mockData(MOCK_DATA).build();\n", javaIndent, javaIndent, javaIndent)
		fmt.Fprintf(b, "%s%s}\n", javaIndent, javaIndent)
	} else {
		fmt.Fprintf(b, "%s%sif (config == null) config = AIFuncConfig.builder().build();\n", javaIndent, javaIndent)
	}
	if cfg.Timeout != nil {
		fmt.Fprintf(b, "%s%sif (config.getTimeoutMs() == 0) config = config.toBuilder().timeoutMs(%d).build();\n", javaIndent, javaIndent, *cfg.Timeout)
	}
	if cfg.MaxRetries != nil {
		fmt.Fprintf(b, "%s%sif (config.getMaxRetries() == 0) config = config.toBuilder().maxRetries(%d).build();\n", javaIndent, javaIndent, *cfg.MaxRetries)
	}
	fmt.Fprintf(b, "\n")
}
// generateNonStreamEntry generates the entry class for a standard (non-streaming) package.
func generateNonStreamEntry(artifact *types.CompiledArtifact, pkgName, baseName string, hasMock bool, enginePkg string, cfg types.AifuncConfig, rootPkg string) (string, error) {
	var b strings.Builder

	funcPascal := toPascalCase(artifact.API.Name)
	inputName  := funcPascal + "Input"
	outputName := funcPascal + "Output"
	typeClass  := baseName + "Types"

	writeJavaHeader(&b, artifact)
	fmt.Fprintf(&b, "package %s.%s;\n\n", rootPkg, pkgName)
	fmt.Fprintf(&b, "import %s.AIFuncConfig;\n", rootPkg)
	fmt.Fprintf(&b, "import %s.Runtime;\n", enginePkg)
	fmt.Fprintf(&b, "import %s.Artifact;\n", enginePkg)
	fmt.Fprintf(&b, "import %s.Types.AIFuncArtifact;\n", enginePkg)
	b.WriteString("import java.util.LinkedHashMap;\nimport java.util.Map;\nimport java.util.concurrent.CompletableFuture;\n\n")

	desc := artifact.API.Description
	if desc == "" {
		desc = artifact.Package.Description
	}
	if desc != "" {
		fmt.Fprintf(&b, "/** %s */\n", desc)
	}
	b.WriteString("/** Generated by aifn — do not edit manually. */\n")
	fmt.Fprintf(&b, "public final class %s {\n\n", baseName)
	fmt.Fprintf(&b, "%sprivate %s() {}\n\n", javaIndent, baseName)

	if hasMock {
		fmt.Fprintf(&b, "%sprivate static final Object MOCK_DATA = %sAifuncMock.MOCK_DATA;\n\n", javaIndent, baseName)
	}

	funcCamel := toCamelCase(artifact.API.Name)
	fmt.Fprintf(&b, "%spublic static CompletableFuture<%s.%s> %s(AIFuncConfig config, %s.%s input) {\n",
		javaIndent, typeClass, outputName, funcCamel, typeClass, inputName)

	writeConfigWiring(&b, hasMock, cfg, baseName)

	fmt.Fprintf(&b, "%s%sAIFuncArtifact artifact = Artifact.fromMap(%sAifunc.ARTIFACT_DATA);\n", javaIndent, javaIndent, baseName)
	fmt.Fprintf(&b, "%s%sMap<String, Object> inputMap = inputToMap(input);\n", javaIndent, javaIndent)
	fmt.Fprintf(&b, "%s%sreturn Runtime.executeAsync(artifact, inputMap, config)\n", javaIndent, javaIndent)
	fmt.Fprintf(&b, "%s%s%s.thenApply(%s::mapToOutput);\n", javaIndent, javaIndent, javaIndent, baseName)
	fmt.Fprintf(&b, "%s}\n\n", javaIndent)

	// inputToMap
	fmt.Fprintf(&b, "%sprivate static Map<String, Object> inputToMap(%s.%s input) {\n", javaIndent, typeClass, inputName)
	fmt.Fprintf(&b, "%s%sMap<String, Object> m = new LinkedHashMap<>();\n", javaIndent, javaIndent)
	inputProps, _ := artifact.API.Input["properties"].(map[string]any)
	for _, key := range sortedKeys(inputProps) {
		field := toCamelCase(key)
		getter := "get" + strings.ToUpper(field[:1]) + field[1:]
		if isRequired(key, artifact.API.Input) {
			fmt.Fprintf(&b, "%s%sm.put(%q, input.%s());\n", javaIndent, javaIndent, key, getter)
		} else {
			fmt.Fprintf(&b, "%s%sif (input.%s() != null) m.put(%q, input.%s());\n", javaIndent, javaIndent, getter, key, getter)
		}
	}
	fmt.Fprintf(&b, "%s%sreturn m;\n%s}\n\n", javaIndent, javaIndent, javaIndent)

	// mapToOutput
	outputProps, _ := artifact.API.Output["properties"].(map[string]any)
	outputRequired := requiredSet(artifact.API.Output)
	var reqOutKeys, optOutKeys []string
	for _, k := range sortedKeys(outputProps) {
		if outputRequired[k] { reqOutKeys = append(reqOutKeys, k) } else { optOutKeys = append(optOutKeys, k) }
	}
	allOutKeys := append(reqOutKeys, optOutKeys...)

	needsUnchecked := false
	for _, key := range allOutKeys {
		prop, ok := outputProps[key].(map[string]any)
		if !ok { continue }
		if javaTypeNeedsUncheckedCast(jsonSchemaToJavaType(prop)) {
			needsUnchecked = true
			break
		}
	}
	if needsUnchecked {
		fmt.Fprintf(&b, "%s@SuppressWarnings(\"unchecked\")\n", javaIndent)
	}
	fmt.Fprintf(&b, "%sprivate static %s.%s mapToOutput(Map<String, Object> m) {\n", javaIndent, typeClass, outputName)
	for _, key := range allOutKeys {
		prop, ok := outputProps[key].(map[string]any)
		if !ok { continue }
		jType := jsonSchemaToJavaType(prop)
		field := toCamelCase(key)
		coerce := javaCoerceExpr(jType, fmt.Sprintf("m.get(%q)", key))
		fmt.Fprintf(&b, "%s%s%s %s = m.containsKey(%q) ? %s : null;\n", javaIndent, javaIndent, jType, field, key, coerce)
	}
	var ctorArgs []string
	for _, key := range allOutKeys {
		if _, ok := outputProps[key].(map[string]any); ok {
			ctorArgs = append(ctorArgs, toCamelCase(key))
		}
	}
	fmt.Fprintf(&b, "%s%sreturn new %s.%s(%s);\n%s}\n}\n", javaIndent, javaIndent, typeClass, outputName, strings.Join(ctorArgs, ", "), javaIndent)

	return b.String(), nil
}
// ---- Java literal serializer ----

// ToJavaMap serialises any value to a Java initializer expression using
// anonymous-subclass-style LinkedHashMap and ArrayList so ordering is preserved
// and there is no 10-key limit.
func ToJavaMap(v any, indent int) string {
	switch val := v.(type) {
	case nil:
		return "null"
	case bool:
		if val {
			return "true"
		}
		return "false"
	case float64:
		if val == float64(int64(val)) {
			return fmt.Sprintf("%dL", int64(val))
		}
		return fmt.Sprintf("%g", val)
	case string:
		return fmt.Sprintf("%q", val)
	case []any:
		return sliceToJava(val, indent)
	case map[string]any:
		return mapToJava(val, indent)
	default:
		data, _ := json.Marshal(val)
		var parsed any
		_ = json.Unmarshal(data, &parsed)
		return ToJavaMap(parsed, indent)
	}
}

func mapToJava(m map[string]any, indent int) string {
	if len(m) == 0 {
		return "java.util.Collections.emptyMap()"
	}
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)

	prefix  := strings.Repeat(javaIndent, indent+1)
	closing := strings.Repeat(javaIndent, indent)

	var b strings.Builder
	b.WriteString("new java.util.LinkedHashMap<String, Object>() {{\n")
	for _, k := range keys {
		fmt.Fprintf(&b, "%sput(%q, %s);\n", prefix, k, ToJavaMap(m[k], indent+1))
	}
	b.WriteString(closing + "}}")
	return b.String()
}

func sliceToJava(arr []any, indent int) string {
	if len(arr) == 0 {
		return "java.util.Collections.emptyList()"
	}
	prefix  := strings.Repeat(javaIndent, indent+1)
	closing := strings.Repeat(javaIndent, indent)

	var b strings.Builder
	b.WriteString("new java.util.ArrayList<Object>(java.util.Arrays.asList(\n")
	for i, val := range arr {
		b.WriteString(prefix + ToJavaMap(val, indent+1))
		if i < len(arr)-1 {
			b.WriteString(",")
		}
		b.WriteString("\n")
	}
	b.WriteString(closing + "))")
	return b.String()
}

// ---- Helpers ----

func writeJavaHeader(b *strings.Builder, artifact *types.CompiledArtifact) {
	b.WriteString("// Generated by aifn — do not edit manually.\n")
	fmt.Fprintf(b, "// Package: %s v%s\n\n", artifact.Package.Name, artifact.Package.Version)
}

func jsonSchemaToJavaType(prop map[string]any) string {
	t, _ := prop["type"].(string)
	switch t {
	case "string":
		return "String"
	case "boolean":
		return "Boolean"
	case "integer":
		return "Integer"
	case "number":
		return "Double"
	case "array":
		if items, ok := prop["items"].(map[string]any); ok {
			return "java.util.List<" + jsonSchemaToJavaType(items) + ">"
		}
		return "java.util.List<Object>"
	case "object":
		return "java.util.Map<String, Object>"
	default:
		return "Object"
	}
}

func javaCoerceExpr(jType, valExpr string) string {
	switch jType {
	case "String":
		return fmt.Sprintf("(%s instanceof String) ? (String) %s : String.valueOf(%s)", valExpr, valExpr, valExpr)
	case "Integer":
		return fmt.Sprintf("(%s instanceof Number) ? ((Number) %s).intValue() : null", valExpr, valExpr)
	case "Double":
		return fmt.Sprintf("(%s instanceof Number) ? ((Number) %s).doubleValue() : null", valExpr, valExpr)
	case "Boolean":
		return fmt.Sprintf("(%s instanceof Boolean) ? (Boolean) %s : null", valExpr, valExpr)
	case "java.util.Map<String, Object>":
		return fmt.Sprintf("(%s instanceof java.util.Map<?,?>) ? (java.util.Map<String, Object>) %s : null", valExpr, valExpr)
	default:
		if strings.HasPrefix(jType, "java.util.List<") {
			return fmt.Sprintf("(%s instanceof java.util.List<?>) ? (%s) %s : null", valExpr, jType, valExpr)
		}
		return fmt.Sprintf("(%s) %s", jType, valExpr)
	}
}

func javaTypeNeedsUncheckedCast(jType string) bool {
	return jType == "java.util.Map<String, Object>" || strings.HasPrefix(jType, "java.util.List<")
}

func toPascalCase(s string) string {
	parts := splitIdentifier(s)
	var b strings.Builder
	for _, p := range parts {
		if len(p) == 0 {
			continue
		}
		b.WriteString(strings.ToUpper(p[:1]) + strings.ToLower(p[1:]))
	}
	return b.String()
}

func toCamelCase(s string) string {
	// If the key already has uppercase letters it is already camelCase — preserve it.
	if strings.ContainsAny(s, "ABCDEFGHIJKLMNOPQRSTUVWXYZ") {
		return s
	}
	parts := splitIdentifier(s)
	var b strings.Builder
	for i, p := range parts {
		if len(p) == 0 {
			continue
		}
		if i == 0 {
			b.WriteString(strings.ToLower(p))
		} else {
			b.WriteString(strings.ToUpper(p[:1]) + strings.ToLower(p[1:]))
		}
	}
	return b.String()
}

func sanitizePackageName(name string) string {
	name = strings.TrimPrefix(name, "@")
	if idx := strings.LastIndex(name, "/"); idx >= 0 {
		name = name[idx+1:]
	}
	name = strings.ReplaceAll(name, "-", "_")
	return strings.ToLower(name)
}

func splitIdentifier(s string) []string {
	s = strings.TrimPrefix(s, "@")
	s = strings.ReplaceAll(s, "-", "_")
	if idx := strings.LastIndex(s, "/"); idx >= 0 {
		s = s[idx+1:]
	}
	return strings.Split(s, "_")
}

func sortedKeys(m map[string]any) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}

func requiredSet(schema map[string]any) map[string]bool {
	set := make(map[string]bool)
	arr, _ := schema["required"].([]any)
	for _, v := range arr {
		if s, ok := v.(string); ok {
			set[s] = true
		}
	}
	return set
}

func isRequired(key string, schema map[string]any) bool {
	return requiredSet(schema)[key]
}
