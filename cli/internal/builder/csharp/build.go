// ---- Types file ----

func generateTypes(artifact *types.CompiledArtifact, ns string) (string, error) {
	var b strings.Builder
	writeCSharpHeader(&b, artifact)
	fmt.Fprintf(&b, "namespace %s;\n\n", ns)
	b.WriteString("using System.Collections.Generic;\n\n")
	fmt.Fprintf(&b, "public static class %sTypes\n{\n", toPascalCase(artifact.Package.Name))

	baseName := toPascalCase(artifact.API.Name)
	writeCSharpPojo(&b, baseName+"Input", artifact.API.Input)
	if !isStreamOutput(artifact) {
		b.WriteString("\n")
		writeCSharpPojo(&b, baseName+"Output", artifact.API.Output)
	}
	b.WriteString("}\n")
	return b.String(), nil
}

// nestedClassInfo describes a nested class to generate inside a Types class.
type nestedClassInfo struct {
	className string
	schema    map[string]any
}

// arrayItemNestedClass returns the nested class name and schema when a property
// is an array whose items are an object with properties, otherwise "".
func arrayItemNestedClass(fieldKey string, prop map[string]any) (string, map[string]any, bool) {
	if t, _ := prop["type"].(string); t != "array" {
		return "", nil, false
	}
	items, ok := prop["items"].(map[string]any)
	if !ok {
		return "", nil, false
	}
	if t, _ := items["type"].(string); t != "object" {
		return "", nil, false
	}
	itemProps, _ := items["properties"].(map[string]any)
	if len(itemProps) == 0 {
		return "", nil, false
	}
	return toPascalCase(singularizeFieldKey(fieldKey)), items, true
}

// singularizeFieldKey turns a plural JSON field key into a singular type-name base
// (entities→entity, keywords→keyword, fields→field).
func singularizeFieldKey(fieldKey string) string {
	switch {
	case strings.HasSuffix(fieldKey, "ies") && len(fieldKey) > 3:
		return fieldKey[:len(fieldKey)-3] + "y"
	case strings.HasSuffix(fieldKey, "ses") && len(fieldKey) > 3:
		return fieldKey[:len(fieldKey)-2] // classes → class
	case strings.HasSuffix(fieldKey, "s") && !strings.HasSuffix(fieldKey, "ss") && len(fieldKey) > 1:
		return fieldKey[:len(fieldKey)-1]
	default:
		return fieldKey + "Item"
	}
}

func writeCSharpPojo(b *strings.Builder, className string, schema map[string]any) {
	props, _ := schema["properties"].(map[string]any)
	required := requiredSet(schema)

	reqKeys := requiredKeysOrdered(schema)
	var optKeys []string
	for _, k := range sortedKeys(props) {
		if !required[k] {
			optKeys = append(optKeys, k)
		}
	}
	allKeys := append(reqKeys, optKeys...)

	// Collect nested classes needed for array-of-object fields.
	var nested []nestedClassInfo
	nestedByField := map[string]string{} // fieldKey -> nested class name
	for _, key := range allKeys {
		prop, ok := props[key].(map[string]any)
		if !ok {
			continue
		}
		if ncName, ncSchema, ok := arrayItemNestedClass(key, prop); ok {
			nestedByField[key] = ncName
			nested = append(nested, nestedClassInfo{ncName, ncSchema})
		}
	}

	fmt.Fprintf(b, "%spublic sealed class %s\n%s{\n", csharpIndent, className, csharpIndent)

	for _, key := range allKeys {
		prop, ok := props[key].(map[string]any)
		if !ok {
			continue
		}
		desc, _ := prop["description"].(string)
		var csType string
		if ncName, isNested := nestedByField[key]; isNested {
			if required[key] {
				csType = "List<" + ncName + ">"
			} else {
				csType = "List<" + ncName + ">?"
			}
		} else {
			csType = jsonSchemaToCSharpType(prop, required[key])
		}
		propName := toPascalCase(key)
		if desc != "" {
			fmt.Fprintf(b, "%s%s/// <summary>%s</summary>\n", csharpIndent, csharpIndent, desc)
		}
		if required[key] {
			fmt.Fprintf(b, "%s%spublic %s %s { get; }\n", csharpIndent, csharpIndent, csType, propName)
		} else {
			fmt.Fprintf(b, "%s%spublic %s %s { get; set; }\n", csharpIndent, csharpIndent, csType, propName)
		}
	}
	b.WriteString("\n")

	// constructor: required params first, then optional with = null
	var params []string
	for _, key := range allKeys {
		prop, ok := props[key].(map[string]any)
		if !ok {
			continue
		}
		var csType string
		if ncName, isNested := nestedByField[key]; isNested {
			if required[key] {
				csType = "List<" + ncName + ">"
			} else {
				csType = "List<" + ncName + ">?"
			}
		} else {
			csType = jsonSchemaToCSharpType(prop, required[key])
		}
		param := toCamelCase(key)
		if required[key] {
			params = append(params, csType+" "+param)
		} else {
			params = append(params, csType+" "+param+" = null")
		}
	}

	if len(params) > 0 || len(reqKeys) > 0 {
		fmt.Fprintf(b, "%s%spublic %s(%s)\n%s%s{\n", csharpIndent, csharpIndent, className, strings.Join(params, ", "), csharpIndent, csharpIndent)
		for _, key := range allKeys {
			if _, ok := props[key].(map[string]any); !ok {
				continue
			}
			propName := toPascalCase(key)
			param := toCamelCase(key)
			fmt.Fprintf(b, "%s%s%s%s = %s;\n", csharpIndent, csharpIndent, csharpIndent, propName, param)
		}
		fmt.Fprintf(b, "%s%s}\n", csharpIndent, csharpIndent)
	} else {
		fmt.Fprintf(b, "%s%spublic %s() {}\n", csharpIndent, csharpIndent, className)
	}

	fmt.Fprintf(b, "%s}\n", csharpIndent)

	// Emit nested classes after the parent class.
	for _, nc := range nested {
		b.WriteString("\n")
		writeCSharpPojo(b, nc.className, nc.schema)
	}
}

// ---- Artifact data file ----

func generateArtifact(artifact *types.CompiledArtifact, ns string) (string, error) {
	var b strings.Builder
	writeCSharpHeader(&b, artifact)
	fmt.Fprintf(&b, "namespace %s;\n\n", ns)
	b.WriteString("using System.Collections.Generic;\n\n")
	fmt.Fprintf(&b, "internal static class %sAifunc\n{\n", toPascalCase(artifact.Package.Name))
	b.WriteString(csharpIndent + "internal static readonly Dictionary<string, object?> ARTIFACT_DATA =\n")
	b.WriteString(csharpIndent + csharpIndent + ToCSharpLiteral(artifact, 2) + ";\n}\n")
	return b.String(), nil
}

