// isStreamOutput returns true when the artifact's output schema declares x-delivery-mode: stream.
func isStreamOutput(artifact *types.CompiledArtifact) bool {
	mode, _ := artifact.API.Output["x-delivery-mode"].(string)
	return mode == "stream"
}

// Generate writes <Name>Types.cs, <Name>Aifunc.cs, <Name>AifuncMock.cs,
// and <Name>.cs for the given compiled artifact into outputDir.
// rootNamespace is the root C# namespace prefix (e.g. "Aifunc").
func Generate(artifact *types.CompiledArtifact, outputDir string, hasMock bool, engineVersion string, cfg types.AifuncConfig, rootNamespace string) error {
	if rootNamespace == "" {
		rootNamespace = defaultNS
	}
	if err := os.MkdirAll(outputDir, dirPerm); err != nil {
		return fmt.Errorf("create output dir: %w", err)
	}

	baseName := toPascalCase(artifact.Package.Name)
	pkgName := sanitizePackageName(artifact.Package.Name)
	ns := packageNamespace(rootNamespace, pkgName)
	engineNS := engineNamespace(rootNamespace, engineVersion)

	type fileSpec struct {
		name      string
		generator func() (string, error)
	}

	files := []fileSpec{
		{baseName + "Types.cs", func() (string, error) {
			return generateTypes(artifact, ns)
		}},
		{baseName + "Aifunc.cs", func() (string, error) {
			return generateArtifact(artifact, ns)
		}},
	}

	if hasMock {
		files = append(files, fileSpec{baseName + "AifuncMock.cs", func() (string, error) {
			return generateMockStub(artifact, ns)
		}})
	}

	files = append(files, fileSpec{baseName + ".cs", func() (string, error) {
		return generateEntry(artifact, ns, baseName, hasMock, engineNS, cfg, rootNamespace)
	}})

	for _, f := range files {
		content, err := f.generator()
		if err != nil {
			return fmt.Errorf("generate %s: %w", f.name, err)
		}
		if err := os.WriteFile(filepath.Join(outputDir, f.name), []byte(content), filePerm); err != nil {
			return fmt.Errorf("write %s: %w", f.name, err)
		}
	}
	return nil
}

// GenerateRootTypes writes AIFuncConfig.cs and AIFuncException.cs to the
// output root directory (namespace "Aifunc" or whatever rootNamespace is).
// These are shared across all installed packages. Optional when the CLI
// promotes them from the engine package instead.
func GenerateRootTypes(outputDir string, rootNamespace string) error {
	if rootNamespace == "" {
		rootNamespace = defaultNS
	}
	if err := os.MkdirAll(outputDir, dirPerm); err != nil {
		return fmt.Errorf("create output dir: %w", err)
	}

	cfgContent := generateAIFuncConfig(rootNamespace)
	if err := os.WriteFile(filepath.Join(outputDir, "AIFuncConfig.cs"), []byte(cfgContent), filePerm); err != nil {
		return fmt.Errorf("write AIFuncConfig.cs: %w", err)
	}

	excContent := generateAIFuncException(rootNamespace)
	if err := os.WriteFile(filepath.Join(outputDir, "AIFuncException.cs"), []byte(excContent), filePerm); err != nil {
		return fmt.Errorf("write AIFuncException.cs: %w", err)
	}
	return nil
}

