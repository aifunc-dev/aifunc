func jsonSchemaToCSharpType(prop map[string]any, required bool) string {
	t, _ := prop["type"].(string)
	switch t {
	case "string":
		if required {
			return "string"
		}
		return "string?"
	case "boolean":
		if required {
			return "bool"
		}
		return "bool?"
	case "integer":
		if required {
			return "int"
		}
		return "int?"
	case "number":
		if required {
			return "double"
		}
		return "double?"
	case "array":
		inner := "object?"
		if items, ok := prop["items"].(map[string]any); ok {
			inner = jsonSchemaToCSharpElementType(items)
		}
		if required {
			return "List<" + inner + ">"
		}
		return "List<" + inner + ">?"
	case "object":
		if required {
			return "Dictionary<string, object?>"
		}
		return "Dictionary<string, object?>?"
	default:
		if required {
			return "object"
		}
		return "object?"
	}
}

// Element types inside List<> are non-nullable when the schema type is known.
func jsonSchemaToCSharpElementType(prop map[string]any) string {
	t, _ := prop["type"].(string)
	switch t {
	case "string":
		return "string"
	case "boolean":
		return "bool"
	case "integer":
		return "int"
	case "number":
		return "double"
	case "array":
		if items, ok := prop["items"].(map[string]any); ok {
			return "List<" + jsonSchemaToCSharpElementType(items) + ">"
		}
		return "List<object?>"
	case "object":
		return "Dictionary<string, object?>"
	default:
		return "object?"
	}
}

func csharpCoerceExpr(csType, tmpVar string, idx int) string {
	base := strings.TrimSuffix(csType, "?")
	switch base {
	case "string":
		if strings.HasSuffix(csType, "?") {
			return fmt.Sprintf("%s is string _s%d ? _s%d : %s?.ToString()", tmpVar, idx, idx, tmpVar)
		}
		return fmt.Sprintf("%s is string _s%d ? _s%d : (%s?.ToString() ?? \"\")", tmpVar, idx, idx, tmpVar)
	case "int":
		return fmt.Sprintf("%s is IConvertible _c%d ? Convert.ToInt32(_c%d) : %s", tmpVar, idx, idx, csharpDefaultExpr(csType))
	case "double":
		return fmt.Sprintf("%s is IConvertible _c%d ? Convert.ToDouble(_c%d) : %s", tmpVar, idx, idx, csharpDefaultExpr(csType))
	case "bool":
		return fmt.Sprintf("%s is bool _b%d ? _b%d : %s", tmpVar, idx, idx, csharpDefaultExpr(csType))
	case "Dictionary<string, object?>":
		return fmt.Sprintf("%s is Dictionary<string, object?> _d%d ? _d%d : %s", tmpVar, idx, idx, csharpDefaultExpr(csType))
	default:
		if strings.HasPrefix(base, "List<") {
			return fmt.Sprintf("%s is %s _l%d ? _l%d : %s", tmpVar, base, idx, idx, csharpDefaultExpr(csType))
		}
		return fmt.Sprintf("%s as %s", tmpVar, csType)
	}
}

func csharpDefaultExpr(csType string) string {
	if strings.HasSuffix(csType, "?") {
		return "null"
	}
	switch csType {
	case "string":
		return `""`
	case "int":
		return "0"
	case "double":
		return "0.0"
	case "bool":
		return "false"
	case "Dictionary<string, object?>":
		return "new Dictionary<string, object?>()"
	default:
		if strings.HasPrefix(csType, "List<") {
			return "new " + csType + "()"
		}
		return "null!"
	}
}

// toPascalCase converts snake_case, kebab-case, or camelCase identifiers to PascalCase.
// camelCase keys (e.g. maxLength) keep internal capitals → MaxLength.
func toPascalCase(s string) string {
	c := toCamelCase(s)
	if c == "" {
		return ""
	}
	return strings.ToUpper(c[:1]) + c[1:]
}

func toCamelCase(s string) string {
	// If the key already has uppercase letters it is already camelCase — preserve it.
	if strings.ContainsAny(s, "ABCDEFGHIJKLMNOPQRSTUVWXYZ") {
		return s
	}
	parts := splitIdentifier(s)
	var b strings.Builder
	for i, p := range parts {
		if len(p) == 0 {
			continue
		}
		if i == 0 {
			b.WriteString(strings.ToLower(p))
		} else {
			b.WriteString(strings.ToUpper(p[:1]) + strings.ToLower(p[1:]))
		}
	}
	return b.String()
}

func sanitizePackageName(name string) string {
	name = strings.TrimPrefix(name, "@")
	if idx := strings.LastIndex(name, "/"); idx >= 0 {
		name = name[idx+1:]
	}
	name = strings.ReplaceAll(name, "-", "_")
	return strings.ToLower(name)
}

func splitIdentifier(s string) []string {
	s = strings.TrimPrefix(s, "@")
	s = strings.ReplaceAll(s, "-", "_")
	if idx := strings.LastIndex(s, "/"); idx >= 0 {
		s = s[idx+1:]
	}
	return strings.Split(s, "_")
}

func sortedKeys(m map[string]any) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}

func requiredSet(schema map[string]any) map[string]bool {
	set := make(map[string]bool)
	arr, _ := schema["required"].([]any)
	for _, v := range arr {
		if s, ok := v.(string); ok {
			set[s] = true
		}
	}
	return set
}

// requiredKeysOrdered returns required keys in their schema declaration order,
// followed by any required keys not listed in the required array (alphabetical).
func requiredKeysOrdered(schema map[string]any) []string {
	arr, _ := schema["required"].([]any)
	seen := make(map[string]bool)
	var ordered []string
	for _, v := range arr {
		if s, ok := v.(string); ok {
			ordered = append(ordered, s)
			seen[s] = true
		}
	}
	// Fallback: any required keys present in properties but not in required array.
	props, _ := schema["properties"].(map[string]any)
	set := requiredSet(schema)
	for _, k := range sortedKeys(props) {
		if set[k] && !seen[k] {
			ordered = append(ordered, k)
		}
	}
	return ordered
}

func isRequired(key string, schema map[string]any) bool {
	return requiredSet(schema)[key]
}
